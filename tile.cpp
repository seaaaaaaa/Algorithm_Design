#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// กำหนดค่า Infinity ให้สูงพอสมควรเพื่อใช้เปรียบเทียบหาค่า min
const int INF = 1e9; 

int main() {
    // ปรับแต่งความเร็ว I/O ของ C++
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    if (!(cin >> n >> m)) return 0;

    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }

    // สร้างตาราง DP ขนาด (n + 1) x (m + 1) และกำหนดค่าเริ่มต้นเป็น INF
    vector<vector<int>> dp(n + 1, vector<int>(m + 1, INF));
    
    // Base Case: ยังไม่เริ่มแลกกระเบื้องเลย พื้นที่รวมเป็น 0 ใช้เงิน 0 บาท
    dp[0][0] = 0;

    // เริ่มคำนวณ DP Bottom-Up
    for (int i = 1; i <= n; ++i) {                     // ลูปพิจารณากระเบื้องทีละแผ่น (แผ่นที่ 1 ถึง n)
        for (int j = 0; j <= m; ++j) {                 // ลูปพื้นที่สะสมที่เป็นไปได้ก่อนหน้า (จาก 0 ถึง m)
            
            // ถ้า state ของพื้นที่ j ก่อนหน้านี้สามารถเป็นไปได้ ค่อยนำมาคิดต่อ
            if (dp[i - 1][j] != INF) {
                
                // ลองแลกกระเบื้องเป็นขนาดใหม่ k (k ต้องเป็นจำนวนเต็มบวก 1, 2, 3...)
                // โดยที่พื้นที่ใหม่รวมกันแล้ว (j + k*k) ต้องไม่เกิน m
                for (int k = 1; j + k * k <= m; ++k) {
                    
                    // คำนวณค่าใช้จ่ายตามโปรโมชั่น |A - B|^2
                    int cost = (a[i] - k) * (a[i] - k);
                    
                    // เลือกเก็บค่าใช้จ่ายที่ถูกที่สุด
                    dp[i][j + k * k] = min(dp[i][j + k * k], dp[i - 1][j] + cost);
                }
            }
        }
    }

    // ตรวจสอบผลลัพธ์ที่กระเบื้องแผ่นสุดท้าย (n) และพื้นที่เท่ากับเป้าหมาย (m)
    if (dp[n][m] == INF) {
        // หากยังเป็น INF แสดงว่าไม่มีวิธีใดเลยที่ทำให้พื้นที่รวมเท่ากับ m ได้
        cout << -1 << "\n";
    } else {
        // พิมพ์ค่าใช้จ่ายที่น้อยที่สุด
        cout << dp[n][m] << "\n";
    }

    return 0;
}
